package com.example.dikson.prototype21menubars;

public class LocationInfo {
    public String x, y, z;
    public String timestamp, tag;

    public LocationInfo(){

    }
    public LocationInfo(String x, String y, String z, String timestamp, String tag){
        this.x = x;
        this.y = y;
        this.z = z;
        this.timestamp = timestamp;
        this.tag = tag;

    }
}
